function UpdatePlaylistUI()
    SendNUIMessage({
        action = "updatePlaylists",
        playlists = Playlists
    })
end

function UpdateConfigUI()
    SendNUIMessage({
        action = "updateConfig",
        config = {
            volume = Config.DefaultVolume,
            enableCost = Config.EnableCost,
            costPerSong = Config.CostPerSong,
            maxDistance = Config.MaxHearingDistance
        }
    })
end

